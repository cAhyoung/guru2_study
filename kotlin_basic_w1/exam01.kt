package kotlin_basic_w1

fun main() {
    var var1 : Int = 10
    var var2 : Float = 10.1f  // 4byte 이용
    var var3 : Double = 10.2  // float형 보다 정밀도가 높음 - > 8byte 이용
    var var4 : Char = '안'  // 문자 1개
    var var5 : String = "안드로이드"  // 여러글자의 문자열

    println(var1)
    println(var2)
    println(var3)
    println(var4)
    println(var5)
}